
# ----------------------------------- area ----------------------------------- #

A = 2 
raiz = 3**0.5
l = float(input('Ingrese el valor de la logitud de un lado del octaedro : '))

area = A*raiz*(l**2)


# ---------------------------------- volumen --------------------------------- #

v = (2/3)* l **3

print(f'el area del octaedro es : {area}')
print(f'el volumen del octaedro es : {v}')
